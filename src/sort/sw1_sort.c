#include "../pushswap.h"

void	sw1_sort(t_sort *sort)
{
	while (not_sorted(sort->a))
	{
		if (sort->a && sort->a->n && sort->a->i > sort->a->n->i)
			if (sort->a->i != 0 && sort->a->n->i != 0)
				swap_a(sort);
		rota_a(sort);
	}
	if (index_location(sort, 0) <= sort->l / 2)
		while (sort->a->i != 0)
			rrta_a(sort);
	else
		while (sort->a->i != 0)
			rota_a(sort);
}
