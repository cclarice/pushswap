#include "pushswap.h"

int numcheck(int *i, int p)
{
	int a;
	int b;

	a = 1;
	b = 1;
	while (a < p)
	{
		while (b < p)
		{
			if (b != a && i[a] == i[b])
				return (1);
			b++;
		}
		a++;
		b = 1;
	}
	return (0);
}

int	main(int c, char *v[])
{
	int	*i;
	int	p;

	i = (int *)malloc(sizeof(int) * c);
	p = 0;
	if (c == 1)
		write(1, "Have on args!\n", 14);
	else if (c-- == 2)
		write(1, "Only one arg!\n", 14);
	else
	{
		while (p++ < c)
			i[p] = ft_atoi(v[p]);
		if (!numcheck(i, p))
		{
			p = 0;
			while (p++ < c)
			{
				ft_putint(p);
				write(1, ": ", 2);
				ft_putint(i[p]);
				write(1, "\n", 1);
			}
		}
		else
		{
			write(1, "Duplicates!!!\n", 14);
			return (1);
		}
	}
	return (2);
}
