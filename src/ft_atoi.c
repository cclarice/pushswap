#include "pushswap.h"

int	ft_atoi(const char *str)
{
	const char	sgn = 1 - (*str == '-') * 2;
	int			ret;

	ret = 0;
	str += (*str == '-' || *str == '+');
	while (*str == '0')
		str++;
	while ((*str >= '0' && *str <= '9' && ret <= 214748363)
		|| (*str >= '0' && *str <= '7' && ret == 214748364))
	{
		ret *= 10;
		ret += *str - '0';
		str++;
	}
	if (*str >= '0' && *str <= '9')
	{
		if (sgn == 1)
			return (2147483647);
		return (-2147483648);
	}
	return (ret * sgn);
}
