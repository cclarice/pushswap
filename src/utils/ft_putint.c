#include "pushswap.h"

void	ft_putchar(const char c)
{
	write(1, &c, 1);
}

void	ft_putint(int i)
{
	if (i <= -1)
	{
		ft_putchar('-');
		if (i == -2147483648)
			write(1, "2147483648", 10);
		else
			ft_putint(i * -1);
	}
	else
	{
		if (i / 10)
			ft_putint(i / 10);
		ft_putchar(i % 10 + '0');
	}
}
